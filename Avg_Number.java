import java.util.*;
public class Avg_Number {
	
	
	    public static void main(String[] args)
	    {
	        Scanner s = new Scanner(System.in);
	        double a = s.nextDouble();
	        double b = s.nextDouble();
	        double avg = 0;
	        avg = (a+b)/2;
	        System.out.format("%.5f",+avg);
	    }
	}
